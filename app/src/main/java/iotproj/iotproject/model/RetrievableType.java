package iotproj.iotproject.model;

/** Represents the different Retrievable types used by the web server
 *
 *  THREAD is a condition thread
 *  DEVICE is an IoT device
 *  SENSOR is an IoT sensor
 *  PROTOCOL is the headers of the sensors
 * */
public enum RetrievableType {

    THREAD,
    DEVICE,
    SENSOR,
    PROTOCOL;

}
